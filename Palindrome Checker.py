# 9. Palindrome Checker: Check if a string is a palindrome.

inp = input("enter a string  :")
reverse = inp [::-1]
if reverse == inp:
  print("it is palindrome")
else:
  print("it is not palindrome")


# OUTPUT :-

# enter a string  :mom
# it is palindrome


# enter a string  :SAI
# it is not palindrome